vector<int> solution(int n, vector< vector<int> > rel) {
	vector< vector<int> > temp(n);
	vector<int> result(n);

	for (int i = 0; i < rel.size(); i++) {
		temp[rel[i].front() - 1].push_back(rel[i].back());
	}

	for (int i = 0; i < temp.size(); i++) {
		result[i] += temp[i].size();
		for (int j = 0; j < temp[i].size(); j++) {
			for (int k = 0; k < temp[temp[i][j] - 1].size(); k++) {
				if(temp[temp[i][j]-1][k] == i+1){
					continue;
				}
				else{
					result[i] += 1;
				}
			}
		}
	}
	return result;
}

void print(vector<int> vec, int n) {
	for (int i = 0; i < n; i++) {
		cout << vec[i] << " ";
	}
}

